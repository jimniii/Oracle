
CREATE TABLE EMP(
    EMPNO NUMBER(4) PRIMARY KEY,
    NAME VARCHAR2(20) NOT NULL,
    GENDER CHAR(1) NOT NULL,
    JOB VARCHAR(20) NOT NULL,
    DEPTNO NUMBER(2),
    REGDATE DATE NOT NULL
    );
    
    DROP TABLE EMP;

CREATE TABLE DEPT(
    DEPTNO NUMBER(2) PRIMARY KEY,
    DNAME VARCHAR2(20) NOT NULL,
    DTEL VARCHAR2(12) NOT NULL
    );

    DROP TABLE DEPT;

