    NO NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, 
    EMPNO NUMBER(4) NOT NULL,
    YEAR NUMBER(4) NOT NULL,
    MONTH NUMBER(2) NOT NULL,
    PRICE NUMBER(10)
    );
    
