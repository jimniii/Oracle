SUM(CASE WHEN T.TR_TYPE = 1 THEN T.TR_AMOUNT ELSE 0 END) AS 총입금액,
SUM(CASE WHEN T.TR_TYPE = 2 THEN T.TR_AMOUNT ELSE 0 END) AS 총출금액
FROM TRANSACTION T
